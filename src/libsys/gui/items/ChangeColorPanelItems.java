package libsys.gui.items;

public class ChangeColorPanelItems 
{
		public javax.swing.JButton apply_btn;
                public javax.swing.JPanel background_panel;
                public javax.swing.JButton changebackgroundcolor_btn;
                public javax.swing.JButton changeforegroundcolor_btn;
                public javax.swing.JPanel foreground_panel;
                public javax.swing.JPanel preview_panel;
                public javax.swing.JTabbedPane prewiewchanges_tabbedpane;
		
		public ChangeColorPanelItems() 
		{
                                preview_panel = new javax.swing.JPanel();
                                changeforegroundcolor_btn = new javax.swing.JButton();
                                changebackgroundcolor_btn = new javax.swing.JButton();
                                prewiewchanges_tabbedpane = new javax.swing.JTabbedPane();
                                background_panel = new javax.swing.JPanel();
                                foreground_panel = new javax.swing.JPanel();
                                apply_btn = new javax.swing.JButton();

                                preview_panel.setBackground(new java.awt.Color(204, 204, 255));

                                changeforegroundcolor_btn.setText("Foreground");

                                changebackgroundcolor_btn.setText("Background");

                                background_panel.setBackground(new java.awt.Color(255, 255, 204));

                                foreground_panel.setBackground(new java.awt.Color(204, 204, 255));
				
				prewiewchanges_tabbedpane.addTab("Preview Changes                                                                                                      ", background_panel);

                                apply_btn.setText("Apply Changes");
		}		
}
